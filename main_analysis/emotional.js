        // Update donut chart
        const donutChart = Chart.getChart('emotionalDonutChart');
        if (donutChart) {
            donutChart.data.datasets[0].data = [
                results.positive.percentage,
                results.neutral.percentage,
                results.negative.percentage
            ];
            donutChart.update();
        }

        // Update half donut chart
        const halfDonutChart = Chart.getChart('emotionalHalfDonutChart');
        if (halfDonutChart) {
            halfDonutChart.data.datasets[0].data = [
                results.positive.percentage,
                results.neutral.percentage,
                results.negative.percentage
            ];
            halfDonutChart.update();
        }

        // Update heatmap chart
        const heatmapChart = Chart.getChart('emotionalHeatmapChart');
        if (heatmapChart) {
            heatmapChart.data.datasets[0].data = [
                results.positive.percentage,
                results.neutral.percentage,
                results.negative.percentage
            ];
            heatmapChart.update();
        }

        // Update radar chart
        const radarChart = Chart.getChart('emotionalRadarChart');
        if (radarChart) {
            radarChart.data.datasets[0].data = [
                results.positive.percentage,
                results.neutral.percentage,
                results.negative.percentage,
                results.positive.percentage,
                results.neutral.percentage
            ];
            radarChart.update();
        }

        // Update word frequency chart
        const wordFrequencyChart = Chart.getChart('emotionalWordFrequencyChart');
        if (wordFrequencyChart) {
            const topEmotions = results.topEmotions.slice(0, 10);
            wordFrequencyChart.data.labels = topEmotions.map(e => e.word);
            wordFrequencyChart.data.datasets[0].data = topEmotions.map(e => e.count);
            wordFrequencyChart.data.datasets[0].backgroundColor = topEmotions.map(e =>
                e.sentiment === 'positive' ? 'rgba(76, 175, 80, 0.8)' : 'rgba(255, 107, 107, 0.8)'
            );
            wordFrequencyChart.update();
        }
